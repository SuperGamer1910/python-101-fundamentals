# Write code to display the area and perimeter
# of a rectangle that has a width of 2.4 and a height of 6.4.

height_of_rectangle = 6.4
width_of_rectangle = 2.4
area_of_rectangle = height_of_rectangle*width_of_rectangle
print(area_of_rectangle)

height_of_rectangle2 = height_of_rectangle*2
width_of_rectangle2 = width_of_rectangle*2
perimeter_of_rectangle = height_of_rectangle2 + width_of_rectangle2
print(perimeter_of_rectangle)